# BULK INSERT в SQL Server — это оператор Transact-SQL, который позволяет быстро загружать большие объемы данных из внешнего файла (например, .csv, .txt, .dat) в таблицу базы данных.
  BULK INSERT ИмяТаблицы
FROM 'путь_к_файлу'
WITH (
    FIELDTERMINATOR = 'разделитель_полей',   -- например, запятая или табуляция
    ROWTERMINATOR = 'разделитель_строк',     -- например, \n
    FIRSTROW = 2,                            -- с какой строки начинать (пропуск заголовка)
    BATCHSIZE = 10000,                       -- размер пакета вставки
    TABLOCK                                  -- блокировка таблицы для ускорения
);
