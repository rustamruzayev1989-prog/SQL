Данные – это любая информация, представленная в форме, пригодной для хранения, обработки и анализа (например, числа, текст, даты).

База данных – организованное хранилище данных, позволяющее эффективно сохранять, изменять и извлекать информацию.

Реляционная база данных (RDB) – база данных, в которой данные хранятся в виде взаимосвязанных таблиц (отношений), связанных между собой с помощью ключей.

Таблица – основная структура хранения данных в реляционной базе, состоящая из строк (записей) и столбцов (полей), где каждая строка представляет отдельный объект или событие, а столбцы – его характеристики
