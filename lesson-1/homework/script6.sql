SQL Server - Хранит данные, обрабатывает запросы, управляет безопасностью и транзакциями
SSMS (SQL Server Management Studio) - Удобный инструмент для администрирования, написания и выполнения SQL-запросов
SQL (Structured Query Language) - Используется для создания баз, таблиц и работы с данными (SELECT, INSERT, UPDATE и т.д.)
