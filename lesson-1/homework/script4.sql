Через графический интерфейс SSMS:
Запускаем SQL Server Management Studio (SSMS) и подключаемся к нашему серверу.
  В Object Explorer щёлкните правой кнопкой по папке Databases → New Database....
  В поле Database name вводим: SchoolDB.
  Нажимаем OK.
  Через T-SQL запрос:
CREATE DATABASE SchoolDB;

