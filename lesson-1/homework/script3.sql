Windows Authentication – вход с использованием учетной записи Windows (интегрированная безопасность).

SQL Server Authentication – вход по логину и паролю, созданным непосредственно в SQL Server.

Mixed Mode - (смешанный режим) некоторых конфигурациях можно встретить , который позволяет использовать оба способа одновременно.
