DELETE - Удаляет данные из таблицы - Можно задать условие WHERE, триггеры срабатывают, структура таблицы сохраняется, Откат (ROLLBACK) - Да (если в транзакции)

TRUNCATE - Удаляет все данные из таблицы целиком - Быстрее чем DELETE, не допускает WHERE, счётчик IDENTITY сбрасывается, Откат (ROLLBACK) - Да (если в транзакции)

DROP - Удаляет объект базы данных (таблицу, базу, представление) полностью - Полностью удаляет таблицу и её структуру, Откат (ROLLBACK) - Нет (невозможно восстановить без резервной копии)
